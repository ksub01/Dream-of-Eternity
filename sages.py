def mission_1_complete():
    """Завершение первой миссии и награда"""
    # отображение информации об награде
    print('Ты справился\nПолучено 100 опыта и 100 золота')
    # получение золота
    hero.gold_receive(100)
    # получение опыта
    hero.exp_receive(100)
    # переход на вторую симмию
    hero.mission_complete(2)
    # пауза перед выходом
    information.pause()


def delirium():
    """Бред, что несут мудрецы, пока заданий нет или оно не выполнено"""
    print('Ты должен помочь нам, во благо этого мира')
    information.pause()
