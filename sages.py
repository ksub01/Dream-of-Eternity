"""Модуль в котором игрок получает задания для прохождения основного квеста и награду за них"""


import hero
import inventory
import information


def dialogue():
    """Функция для получения заданий и получение награды"""
    print('Здесь вы получаете поручения от основателей, чтобы помочь людям в это нелёгкое время, по заданым заданиям')
    information.pause()
    # если пришёл первый раз
    if hero.statistics['mission'] == 0:
        # миссия 1 начинается
        mission_1_start()
    elif hero.statistics['mission'] == 1 and inventory.spend_items('Сердце гоблина воина', 6)\
            == 1:
        # если хватает вещей и активна первая миссия, то задание 1 завершается
        mission_1_complete()
    else:
        # слова мудреца, работают если игрок пока не выполнил задание, выводит в город
        delirium()
        pass


